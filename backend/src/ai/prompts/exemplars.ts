/**
 * Gold Exemplars — human-approved reference stories used as few-shot examples to
 * steer generation craft: lively voice, gentle humour, safe conflict,
 * show-don't-tell, and the moral stated only once.
 *
 * See `Gold Exemplar` in CONTEXT.md and docs/adr/0004-safe-conflict-boundary.md.
 * One exemplar is injected into the generation prompt, chosen by learning goal.
 * The model must match the CRAFT — never copy the plot, names, or setting.
 */

export interface Exemplar {
  /** Learning-goal titles this exemplar best fits (matched case-insensitively). */
  readonly goalTitles: readonly string[];
  /** The full gold story, formatted for few-shot injection. */
  readonly text: string;
}

const COURAGE: Exemplar = {
  goalTitles: ['Смелость', 'Преодоление страха темноты'],
  text: `Название: «Миша и ночная тень»  (тип конфликта: внутренний страх)
[Завязка] Днём Миша был самым храбрым на свете. Он сражался с драконами из диванных подушек, прыгал с кровати прямо в открытый космос и не боялся ни-че-го. Ну… почти ничего. Потому что вечером, когда мама гасила свет, вся его храбрость куда-то пряталась.
[Конфликт] В дальнем углу сидела Большая Тёмная Тень. Выглядела она крайне подозрительно. Миша натянул одеяло до самых бровей и прошептал: «Я тебя вижу. И совсем-совсем не боюсь… ну, чуть-чуточку». А сердце стучало, как барабан на параде.
[Внутренняя борьба] Можно было позвать маму. Но настоящие рыцари маму не зовут! Или… всё-таки зовут? Миша лежал и спорил сам с собой. А Тень всё торчала в углу и делала вид, будто она тут совершенно ни при чём.
[Поворот] Тогда Миша набрал смелости — целую полную горсть. «Так, сейчас разберёмся», — буркнул он самым храбрым шёпотом. Он сжал кулаки, зажмурился покрепче, а потом одним глазком, чуть-чуть, заглянул в тёмный угол.
[Развязка] А там… никакого чудовища! Там сидел его плюшевый Мишка и глядел немного обиженно. «Ой. Так это же ты», — выдохнул Миша и прыснул со смеху. Грозная Тень оказалась обычным мишкой в свете луны.
[Закрепление через действие] Миша сгрёб Мишку в охапку и усадил рядом на подушку. «Будешь моим ночным охранником», — решил он важно. Теперь он знал секрет: если страшно — вдохни поглубже и загляни поближе.
[Финал] Быть смелым — это заглянуть в тёмный угол, даже когда сердечко скачет зайчиком.
Вопросы: 1. Каким храбрым Миша был днём? 2. Что напугало его вечером? 3. Что он сделал, чтобы проверить угол? 4. Кто оказался «страшным чудовищем»? 5. А что помогает тебе, когда в темноте становится страшно?`,
};

const KINDNESS: Exemplar = {
  goalTitles: ['Доброта', 'Делиться с другими', 'Сочувствие', 'Забота о младших'],
  text: `Название: «Соня и новенький»  (тип конфликта: социальный / эмпатия)
[Завязка] Соня обожала свою площадку. Тут была её любимая горка, её лучшие друзья и её новенькое ведёрко с лопаткой — блестящее, красное, самое-самое. Соня берегла его как настоящее сокровище и никому-никому не давала. Даже на минуточку.
[Конфликт] И вдруг Соня заметила мальчика. Он сидел один на краю песочницы и просто водил пальцем по песку. Никто с ним не играл. И Соне стало грустно за него — будто это её саму сегодня никто не позвал играть.
[Внутренняя борьба] «Подойти? А вдруг он не захочет играть?» — думала Соня. Ноги будто приклеились к горке. Подойти к незнакомому мальчику было почему-то страшнее, чем съехать с самой высокой-превысокой горки.
[Поворот] Соня сделала глубокий вдох и крепче сжала своё блестящее ведёрко. А потом… взяла и пошла. «Привет! Хочешь печь со мной куличики?» — выпалила она и протянула ему лопатку. Свою самую любимую.
[Развязка] Мальчик поднял глаза и улыбнулся так широко, будто кто-то включил солнце. «Очень хочу!» — сказал он. И вот они уже вместе лепили самый большой кулич на свете, и Сонина грусть растаяла без следа.
[Закрепление через действие] Теперь у Сони стало на одного друга больше. Оказалось, делиться лопаткой совсем не жалко — наоборот, весело. А самое лучшее сокровище на площадке — это вовсе не красное ведёрко.
[Финал] Доброта — это сделать маленький шаг навстречу тому, кому грустно.
Вопросы: 1. Что Соня берегла как сокровище? 2. Почему ей стало грустно за мальчика? 3. Что оказалось страшнее, чем съехать с высокой горки? 4. Чем Соня поделилась с новым другом? 5. А как ты можешь поднять настроение тому, кому грустно?`,
};

const INDEPENDENCE: Exemplar = {
  goalTitles: ['Самостоятельность', 'Настойчивость', 'Трудолюбие'],
  text: `Название: «Тёма и непослушные шнурки»  (тип конфликта: навык / настойчивость)
[Завязка] Тёма решил, что он уже совсем большой. Сам надевал куртку, сам нёс свой рюкзак и страшно сердился, когда ему помогали. «Я са-ам!» — было его самое любимое слово на свете. Но оставалась одна вредная штука, которая никак не поддавалась.
[Конфликт] Шнурки. Эти противные шнурки! Они извивались в пальцах, как два сонных червяка, и ни за что не хотели завязываться. Тёма пыхтел, сопел, и кончик носа у него стал красным от стараний.
[Внутренняя борьба] «Мам, помоги!» — чуть не крикнул он. Но вовремя сжал губы. Нет. Сегодня он завяжет их сам, даже если на это уйдёт целая вечность. Тёма попробовал ещё разок. И ещё. А узелок снова взял и рассыпался.
[Поворот] Тогда Тёма вспомнил мамин секрет: сначала петелька, как заячье ушко, потом второе, а потом — обнять и нырнуть. Он высунул язык от усердия и стал делать всё медленно-медленно. Петелька… второе ушко… обнять…
[Развязка] И вдруг — щёлк! — шнурки послушно завязались в крепкий бантик. Тёма не поверил своим глазам. «Я сам! Я СА-АМ завязал!» — закричал он на весь дом и подпрыгнул чуть ли не до потолка.
[Закрепление через действие] Тёма помчался хвастаться маме, и бантик гордо болтался на ботинке. Оказалось, если не сдаваться и пробовать снова и снова, даже самые непослушные шнурки в конце концов слушаются.
[Финал] Быть самостоятельным — это пробовать снова и снова, пока не получится самому.
Вопросы: 1. Какое слово было у Тёмы самым любимым? 2. Что никак не хотело завязываться? 3. Что Тёма чуть не крикнул маме — и почему передумал? 4. Какой секрет помог завязать шнурки? 5. А что ты уже умеешь делать сам?`,
};

const EXEMPLARS: readonly Exemplar[] = [COURAGE, KINDNESS, INDEPENDENCE];

/**
 * Pick the exemplar whose goal list contains the given learning-goal title.
 * Falls back to the courage exemplar (a clean internal-conflict model).
 */
export const pickExemplar = (goalTitle: string): Exemplar => {
  const normalized = goalTitle.trim().toLowerCase();
  return EXEMPLARS.find((e) => e.goalTitles.some((t) => t.toLowerCase() === normalized)) ?? COURAGE;
};
